import fs from "node:fs";
import path from "node:path";
import vm from "node:vm";

const projectRoot = process.cwd();
const menuFile = path.join(projectRoot, "src", "content", "knowledge-menu.ts");
const outDir = path.join(projectRoot, "content", "knowledge");
const slugMapFile = path.join(
  projectRoot,
  "src",
  "content",
  "knowledge-slug-map.ts",
);

function cyrb53(str, seed = 0) {
  let h1 = 0xdeadbeef ^ seed,
    h2 = 0x41c6ce57 ^ seed;
  for (let i = 0, ch; i < str.length; i++) {
    ch = str.charCodeAt(i);
    h1 = Math.imul(h1 ^ ch, 2654435761);
    h2 = Math.imul(h2 ^ ch, 1597334677);
  }
  h1 =
    Math.imul(h1 ^ (h1 >>> 16), 2246822507) ^
    Math.imul(h2 ^ (h2 >>> 13), 3266489909);
  h2 =
    Math.imul(h2 ^ (h2 >>> 16), 2246822507) ^
    Math.imul(h1 ^ (h1 >>> 13), 3266489909);
  return 4294967296 * (2097151 & h2) + (h1 >>> 0);
}

function safeSlugFromTitle(title) {
  // Stable, short, URL-safe slug derived from title
  const hash = cyrb53(title).toString(36);
  return `k-${hash}`;
}

function loadMenu() {
  const src = fs.readFileSync(menuFile, "utf8");
  // Extract the array literal only (ignores TS types/exports safely)
  const idx = src.indexOf("knowledgeMenu");
  if (idx < 0) throw new Error("knowledgeMenu not found in knowledge-menu.ts");
  const start = src.indexOf("[", idx);
  const end = src.indexOf("];", start);
  if (start < 0 || end < 0) throw new Error("knowledgeMenu array not found");
  const arrLiteral = src.slice(start, end + 1);

  const wrapped = `const knowledgeMenu = ${arrLiteral};\nmodule.exports = { knowledgeMenu };`;
  const context = { module: { exports: {} } };
  vm.createContext(context);
  vm.runInContext(wrapped, context, { filename: "knowledge-menu.ts" });
  return context.module.exports.knowledgeMenu;
}

function collectLeafNames(nodes, acc = new Set()) {
  for (const n of nodes) {
    if (n.children && n.children.length) collectLeafNames(n.children, acc);
    else acc.add(n.name);
  }
  return acc;
}

function ensureDir(p) {
  fs.mkdirSync(p, { recursive: true });
}

function writeIfMissing(filePath, content) {
  if (fs.existsSync(filePath)) return false;
  fs.writeFileSync(filePath, content, "utf8");
  return true;
}

function frontmatter({ title, slug }) {
  const today = new Date().toISOString().slice(0, 10);
  return `---\n` +
    `title: "${title.replaceAll('"', '\\"')}"\n` +
    `slug: "${slug}"\n` +
    `date: "${today}"\n` +
    `type: "note"\n` +
    `tags: []\n` +
    `summary: ""\n` +
    `---\n\n` +
    `# ${title}\n\n` +
    `> TODO: paste your content here.\n`;
}

const menu = loadMenu();
const leafNames = [...collectLeafNames(menu)].sort((a, b) =>
  a.localeCompare(b, "zh-Hans-CN"),
);

ensureDir(outDir);

const slugMap = {};
let created = 0;
for (const name of leafNames) {
  const slug = safeSlugFromTitle(name);
  slugMap[name] = slug;
  const filePath = path.join(outDir, `${slug}.md`);
  if (writeIfMissing(filePath, frontmatter({ title: name, slug }))) created++;
}

const ts =
  `// Auto-generated by scripts/gen-knowledge-stubs.mjs\n` +
  `// Chinese menu title -> URL-safe slug (and file name).\n` +
  `export const knowledgeSlugMap: Record<string, string> = ${JSON.stringify(
    slugMap,
    null,
    2,
  )} as const;\n`;
fs.writeFileSync(slugMapFile, ts, "utf8");

console.log(
  `Knowledge stubs: created ${created}, total leaves ${leafNames.length}. Wrote slug map.`,
);

